def _clone_socket(socket: Mapping[str, Any], identifier: str) -> dict[str, Any]:
    clone = {
        key: value
        for key, value in socket.items()
        if key not in {"socket_hash", "arm", "topology_families"}
    }
    clone["socket_id"] = identifier
    clone["generation"] = f"control:{identifier}:{socket['socket_id']}"
    return _rehash_socket(clone)


def _tag_topology(socket: Mapping[str, Any], *, arm: str) -> dict[str, Any]:
    tagged = dict(socket)
    tagged["arm"] = arm
    tagged["topology_families"] = _topology_families(socket)
    return tagged


def entry_key(entry: Mapping[str, Any]) -> str:
    return f"layer-{int(entry['layer'])}:family-{entry['family']}"


def _parameter_entry_key(name: str, candidate: Mapping[str, Any]) -> str | None:
    for entry in candidate["entries"]:
        layer_prefix = f"model.layers.{int(entry['layer'])}."
        if layer_prefix not in name:
            continue
        family = str(entry["family"])
        if family == "A" and (".self_attn.q_proj." in name or ".self_attn.v_proj." in name):
            return entry_key(entry)
        if family == "B" and ".self_attn.o_proj." in name:
            return entry_key(entry)
        if family == "C" and ".mlp.down_proj." in name:
            return entry_key(entry)
    return None


def gradient_norms_by_entry(gradients: Any, candidate: Mapping[str, Any]) -> dict[str, float]:
    squared = {entry_key(entry): 0.0 for entry in candidate["entries"]}
    for name, value in tree_flatten(gradients):
        key = _parameter_entry_key(name, candidate)
        if key is not None:
            squared[key] += float(mx.sum(value.astype(mx.float32) ** 2))
    return {key: math.sqrt(value) for key, value in squared.items()}


def mask_frozen_entry_gradients(
    gradients: Any,
    candidate: Mapping[str, Any],
    frozen_entries: set[str],
) -> Any:
    if not frozen_entries:
        return gradients
    flattened = []
    for name, value in tree_flatten(gradients):
        key = _parameter_entry_key(name, candidate)
        flattened.append((name, mx.zeros_like(value) if key in frozen_entries else value))
    return tree_unflatten(flattened)


def _topology_families(socket: Mapping[str, Any]) -> list[str]:
    entries = socket["entries"]
    family_counts = {family: sum(entry["family"] == family for entry in entries) for family in ("A", "B", "C")}
    mean_layer = float(np.mean([entry["layer"] for entry in entries]))
    labels = ["distributed"] if len({_band_for(entry["layer"]) for entry in entries}) >= 4 else []
    if family_counts["C"] >= len(entries) / 2:
        labels.append("MLP-heavy")
    if family_counts["A"] + family_counts["B"] >= len(entries) * 2 / 3:
        labels.append("attention-heavy")
    if not labels or (family_counts["C"] and family_counts["A"] + family_counts["B"]):
        labels.append("mixed")
    labels.append("early-layer" if mean_layer < 8 else "late-layer" if mean_layer > 14 else "middle-layer")
    return sorted(set(labels))


def _band_for(layer: int) -> int:
    return next(index for index, (low, high) in enumerate(BANDS) if low <= layer <= high)


def _rehash_socket(socket: Mapping[str, Any]) -> dict[str, Any]:
    body = {key: value for key, value in socket.items() if key != "socket_hash"}
    return {**body, "socket_hash": _hash(body)}


def _prompt_tokens(tokenizer: Any, prompt: str) -> list[int]:
    return list(tokenizer.apply_chat_template([{"role": "user", "content": prompt}], add_generation_prompt=True, return_dict=False))


def _binary_entropy(probability: float) -> float:
    if probability <= 0 or probability >= 1:
        return 0.0
    return -probability * math.log(probability) - (1 - probability) * math.log(1 - probability)


def _curve_auc(curve: Sequence[Mapping[str, Any]]) -> float:
    if len(curve) < 2:
        return 0.0
    x = np.asarray([row["generated_tokens"] for row in curve], dtype=float)
    y = np.asarray([row["behavioral_accuracy"] for row in curve], dtype=float)
    return float(np.trapezoid(y, x))


def _latest_checkpoint(root: Path) -> Path | None:
    pointer = root / "LATEST"
    if not pointer.is_file():
        return None
    candidate = root / pointer.read_text(encoding="ascii").strip()
    return candidate if (candidate / "COMPLETE").is_file() else None


def _update_handoff(root: Path, config: Mapping[str, Any], *, stage: str, current: str | None, next_command: str) -> None:
    completed = [str(path.relative_to(root)) for path in sorted((root / "jobs").glob("*/seed-*/latest_result.json"))] if (root / "jobs").is_dir() else []
    failures = _read_json(root / "failure_summary.json").get("failures", []) if (root / "failure_summary.json").is_file() else []
    packet = {
        "schema": f"{SCHEMA}.resume_packet.v1",
        "run_id": config["run_id"],
        "git_commit": _git_commit(),
        "current_stage": stage,
        "current_candidate": current,
        "completed_jobs": completed[-100:],
        "failed_jobs": failures[-20:],
        "latest_valid_checkpoint": _latest_checkpoint_any(root),
        "next_executable_command": next_command,
        "provisional_leading_candidates": _provisional_leaders(root),
        "artifact_hashes": _compact_artifact_hashes(root),
        "last_update_time": utc_now(),
    }
    encoded = json.dumps(packet, sort_keys=True).encode()
    if len(encoded) > 32 * 1024:
        raise ValueError("resume_packet.json exceeds 32 KB")
    _write_atomic_json(root / "resume_packet.json", packet)
    (root / "next_action.md").write_text(f"# Next Action\n\nStage: `{stage}`\n\n{next_command}\n", encoding="utf-8")
    if not (root / "latest_result.json").is_file():
        _write_atomic_json(
            root / "latest_result.json",
            {"schema": f"{SCHEMA}.latest_result.v1", "status": "RUNNING", "stage": stage, "updated_at": utc_now()},
        )


