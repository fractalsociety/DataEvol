def _topology_families(socket: Mapping[str, Any]) -> list[str]:
    entries = socket["entries"]
    family_counts = {family: sum(entry["family"] == family for entry in entries) for family in ("A", "B", "C")}
    mean_layer = float(np.mean([entry["layer"] for entry in entries]))
    labels = ["distributed"] if len({_band_for(entry["layer"]) for entry in entries}) >= 4 else []
    if family_counts["C"] >= len(entries) / 2:
        labels.append("MLP-heavy")
    if family_counts["A"] + family_counts["B"] >= len(entries) * 2 / 3:
        labels.append("attention-heavy")
    if not labels or (family_counts["C"] and family_counts["A"] + family_counts["B"]):
        labels.append("mixed")
    labels.append("early-layer" if mean_layer < 8 else "late-layer" if mean_layer > 14 else "middle-layer")
    return sorted(set(labels))


def _band_for(layer: int) -> int:
    return next(index for index, (low, high) in enumerate(BANDS) if low <= layer <= high)


def _rehash_socket(socket: Mapping[str, Any]) -> dict[str, Any]:
    body = {key: value for key, value in socket.items() if key != "socket_hash"}
    return {**body, "socket_hash": _hash(body)}


def _prompt_tokens(tokenizer: Any, prompt: str) -> list[int]:
    return list(tokenizer.apply_chat_template([{"role": "user", "content": prompt}], add_generation_prompt=True, return_dict=False))


def _binary_entropy(probability: float) -> float:
    if probability <= 0 or probability >= 1:
        return 0.0
    return -probability * math.log(probability) - (1 - probability) * math.log(1 - probability)


def _curve_auc(curve: Sequence[Mapping[str, Any]]) -> float:
    if len(curve) < 2:
        return 0.0
    x = np.asarray([row["generated_tokens"] for row in curve], dtype=float)
    y = np.asarray([row["behavioral_accuracy"] for row in curve], dtype=float)
    return float(np.trapezoid(y, x))


def _latest_checkpoint(root: Path) -> Path | None:
    pointer = root / "LATEST"
    if not pointer.is_file():
        return None
    candidate = root / pointer.read_text(encoding="ascii").strip()
    return candidate if (candidate / "COMPLETE").is_file() else None


def _update_handoff(root: Path, config: Mapping[str, Any], *, stage: str, current: str | None, next_command: str) -> None:
    completed = [str(path.relative_to(root)) for path in sorted((root / "jobs").glob("*/seed-*/latest_result.json"))] if (root / "jobs").is_dir() else []
    failures = _read_json(root / "failure_summary.json").get("failures", []) if (root / "failure_summary.json").is_file() else []
    packet = {
        "schema": f"{SCHEMA}.resume_packet.v1",
        "run_id": config["run_id"],
        "git_commit": _git_commit(),
        "current_stage": stage,
        "current_candidate": current,
        "completed_jobs": completed[-100:],
        "failed_jobs": failures[-20:],
        "latest_valid_checkpoint": _latest_checkpoint_any(root),
        "next_executable_command": next_command,
        "provisional_leading_candidates": _provisional_leaders(root),
        "artifact_hashes": _compact_artifact_hashes(root),
        "last_update_time": utc_now(),
    }
    encoded = json.dumps(packet, sort_keys=True).encode()
    if len(encoded) > 32 * 1024:
        raise ValueError("resume_packet.json exceeds 32 KB")
    _write_atomic_json(root / "resume_packet.json", packet)
    (root / "next_action.md").write_text(f"# Next Action\n\nStage: `{stage}`\n\n{next_command}\n", encoding="utf-8")
    if not (root / "latest_result.json").is_file():
        _write_atomic_json(
            root / "latest_result.json",
            {"schema": f"{SCHEMA}.latest_result.v1", "status": "RUNNING", "stage": stage, "updated_at": utc_now()},
        )


def _write_failure(root: Path, summary: str, detail: str) -> None:
    path = root / "failure_summary.json"
    current = _read_json(path) if path.is_file() else {"schema": f"{SCHEMA}.failures.v1", "failures": []}
    current["failures"].append({"time": utc_now(), "summary": summary, "detail": detail[-4000:]})
    _write_atomic_json(path, current)


def _latest_checkpoint_any(root: Path) -> str | None:
    pointers = sorted((root / "jobs").glob("*/seed-*/checkpoints/LATEST"), key=lambda path: path.stat().st_mtime) if (root / "jobs").is_dir() else []
    if not pointers:
        return None
    pointer = pointers[-1]
    return str((pointer.parent / pointer.read_text().strip()).relative_to(root))


def _provisional_leaders(root: Path) -> list[dict[str, Any]]:
    results = [_read_json(path) for path in (root / "jobs").glob("*/seed-*/latest_result.json")] if (root / "jobs").is_dir() else []
    results.sort(key=lambda row: -float(row["selection_score"]))
    return [{"candidate_id": row["candidate_id"], "score": row["selection_score"]} for row in results[:5]]


def _compact_artifact_hashes(root: Path) -> dict[str, str]:
    names = ("trainer_audit.json", "candidate_manifest.json", "hypothesis_verdicts.json")
    return {name: _file_hash(root / name) for name in names if (root / name).is_file()}


def _validate_config(config: Mapping[str, Any]) -> None:
    if config.get("schema") != f"{SCHEMA}.config.v1":
        raise ValueError("unsupported RL socket config schema")
    if int(config["search"]["candidates_per_arm"]) < 3:
        raise ValueError("each search arm requires at least three candidates")
    if int(config["rollout"]["group_size"]) < 8:
        raise ValueError("rollout group size must be at least eight")
    if float(config["adapter"]["maximum_parameter_mismatch"]) != 0.005:
        raise ValueError("parameter mismatch must be exactly 0.5%")
    selection = config["selection"]
    weights = [float(value) for key, value in selection.items() if key.endswith("_weight")]
    if not math.isclose(sum(weights), 1.0):
        raise ValueError("selection weights must sum to one")


def _request_termination(signum: int, frame: Any) -> None:
    del signum, frame
    global TERMINATE_REQUESTED
    TERMINATE_REQUESTED = True


def _repo_root() -> Path:
    return Path(__file__).resolve().parents[3]


def _git_commit() -> str:
    return subprocess.check_output(["git", "rev-parse", "HEAD"], cwd=_repo_root(), text=True).strip()


def _read_json(path: Path) -> dict[str, Any]:
    return json.loads(path.read_text(encoding="utf-8"))


def _read_jsonl(path: Path) -> list[dict[str, Any]]:
    return [json.loads(line) for line in path.read_text(encoding="utf-8").splitlines() if line.strip()]


def _hash(value: Mapping[str, Any]) -> str:
    return hashlib.sha256(json.dumps(dict(value), sort_keys=True, separators=(",", ":")).encode()).hexdigest()


def _file_hash(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


def _write_atomic_json(path: Path, value: Mapping[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    temporary = path.with_suffix(path.suffix + ".tmp")
    temporary.write_text(json.dumps(dict(value), indent=2, sort_keys=True) + "\n", encoding="utf-8")
    temporary.replace(path)


