def _promote_candidates(root: Path, candidates: Sequence[Mapping[str, Any]], config: Mapping[str, Any], stage: str) -> list[str]:
    ranked = _rank_candidates(root, candidates, config)
    if stage == "stage_c":
        return [row["socket"]["socket_id"] for row in ranked]
    eligible_rows = [row for row in ranked if row["eligible"]]
    if not eligible_rows:
        raise RuntimeError(f"all candidates were unhealthy at {stage}")
    leader_lower = max(row["confidence_lower"] for row in eligible_rows)
    margin = float(config["search"]["promotion_margin"])
    retained = [row for row in eligible_rows if row["confidence_upper"] >= leader_lower - margin]
    for family in FAMILIES:
        family_rows = [row for row in eligible_rows if family in row["socket"]["topology_families"]]
        if family_rows and not any(row in retained for row in family_rows):
            retained.append(family_rows[0])
    for arm in ("guided_search", "random_search"):
        arm_rows = [row for row in ranked if row["arm"] == arm]
        if arm_rows and not any(row["arm"] == arm for row in retained):
            retained.append(arm_rows[0])
    return sorted({row["socket"]["socket_id"] for row in retained})


def _rank_candidates(root: Path, candidates: Sequence[Mapping[str, Any]], config: Mapping[str, Any]) -> list[dict[str, Any]]:
    rows = []
    for candidate in candidates:
        results = []
        for seed in config["search"]["seeds"]:
            path = root / "jobs" / candidate["socket_id"] / f"seed-{seed}" / "latest_result.json"
            if path.is_file():
                results.append(_read_json(path))
        if not results:
            continue
        scores = [float(result["selection_score"]) for result in results]
        raw_mean = float(np.mean(scores))
        stability = max(0.0, 1.0 - float(np.std([result["behavioral_accuracy"] for result in results])))
        mean = raw_mean - 0.05 + 0.05 * stability
        standard_error = float(np.std(scores) / math.sqrt(len(scores))) if len(scores) > 1 else 0.05
        rows.append({
            "socket": candidate,
            "arm": candidate["arm"],
            "mean_score": mean,
            "confidence_lower": mean - 1.96 * standard_error,
            "confidence_upper": mean + 1.96 * standard_error,
            "seed_stability": stability,
            "eligible": not any(bool(result["unhealthy"]) for result in results),
        })
    rows.sort(key=lambda row: (not row["eligible"], -row["mean_score"], row["socket"]["socket_hash"]))
    return rows


def _hypothesis_verdicts(
    root: Path,
    sockets: Mapping[str, Mapping[str, Any]],
    best_single: Mapping[str, Any],
    random_distribution: Sequence[Mapping[str, Any]],
    seeds: Sequence[int],
    config: Mapping[str, Any],
) -> dict[str, Any]:
    methods: dict[str, list[dict[str, Any]]] = {}
    for method, socket in sockets.items():
        identifier = socket["socket_id"]
        methods[method] = [_read_json(root / "jobs" / identifier / f"seed-{seed}" / "latest_result.json") for seed in seeds]
    best_single_id = best_single["socket_id"]
    methods["best_single_layer"] = [
        _read_json(root / "jobs" / best_single_id / f"seed-{seed}" / "latest_result.json")
        for seed in seeds
    ]
    random_rows = []
    for socket in random_distribution:
        for seed in seeds:
            random_rows.append(_read_json(root / "jobs" / socket["socket_id"] / f"seed-{seed}" / "latest_result.json"))
    searched = methods["rl_searched_socket"]
    intervals = {
        "H1_SPARSE_VS_UNIFORM": _paired_interval(searched, methods["uniform_lora"]),
        "H2_SOCKET_VS_SINGLE_LAYER": _paired_interval(searched, methods["best_single_layer"]),
        "H4_SFT_TOPOLOGY_TRANSFER": _distribution_interval(methods["sft_topology_fresh_weights"], random_rows),
        "H5_SFT_WEIGHT_CONTINUATION": _paired_interval(methods["sft_topology_sft_weights"], methods["sft_topology_fresh_weights"]),
    }
    guided_search_rows = []
    for candidate_dir in (root / "jobs").glob("guided-*"):
        for seed in config["search"]["seeds"]:
            result = candidate_dir / f"seed-{seed}" / "latest_result.json"
            if result.is_file():
                guided_search_rows.append(_read_json(result))
    intervals["H3_SEARCH_VS_RANDOM_SEARCH"] = _distribution_interval(guided_search_rows, random_rows)
    noninferiority = float(config["confirmation"]["noninferiority_margin"])
    verdicts = {
        "H1_SPARSE_VS_UNIFORM": "SUPPORTED" if intervals["H1_SPARSE_VS_UNIFORM"]["lower"] > 0 else "REJECTED",
        "H2_SOCKET_VS_SINGLE_LAYER": "SUPPORTED" if intervals["H2_SOCKET_VS_SINGLE_LAYER"]["lower"] >= -noninferiority else "REJECTED",
        "H3_SEARCH_VS_RANDOM_SEARCH": "SUPPORTED" if intervals["H3_SEARCH_VS_RANDOM_SEARCH"]["lower"] > 0 else "REJECTED",
        "H4_SFT_TOPOLOGY_TRANSFER": "SUPPORTED" if intervals["H4_SFT_TOPOLOGY_TRANSFER"]["lower"] > 0 else "REJECTED",
        "H5_SFT_WEIGHT_CONTINUATION": "SUPPORTED" if intervals["H5_SFT_WEIGHT_CONTINUATION"]["lower"] > 0 else "REJECTED",
    }
    if verdicts["H1_SPARSE_VS_UNIFORM"] == "SUPPORTED" and verdicts["H4_SFT_TOPOLOGY_TRANSFER"] == "SUPPORTED":
        overall = "A. Sparse RL sockets work and topology transfers."
    elif verdicts["H1_SPARSE_VS_UNIFORM"] == "SUPPORTED" and verdicts["H3_SEARCH_VS_RANDOM_SEARCH"] == "SUPPORTED":
        overall = "B. Sparse RL sockets work, but RL needs a different topology."
    elif verdicts["H1_SPARSE_VS_UNIFORM"] == "SUPPORTED":
        overall = "C. Sparse placement works, but guided search adds no value."
    elif verdicts["H2_SOCKET_VS_SINGLE_LAYER"] == "REJECTED":
        overall = "D. Single-layer adaptation remains best."
    else:
        overall = "F. Results remain inconclusive."
    body = {
        "schema": f"{SCHEMA}.hypothesis_verdicts.v1",
        "trainer_audit_hash": _read_json(root / "trainer_audit.json")["audit_hash"],
        "verdicts": verdicts,
        "intervals": intervals,
        "best_single_layer": best_single_id,
        "overall_conclusion": overall,
        "completed_at": utc_now(),
    }
    return {**body, "verdict_hash": _hash(body)}


def _paired_interval(left: Sequence[Mapping[str, Any]], right: Sequence[Mapping[str, Any]], draws: int = 10_000) -> dict[str, float]:
    count = min(len(left), len(right))
    differences = np.concatenate([_prompt_scores(left[index]) - _prompt_scores(right[index]) for index in range(count)])
    rng = np.random.default_rng(7102)
    bootstrap = np.mean(rng.choice(differences, size=(draws, len(differences)), replace=True), axis=1)
    return {"mean_difference": float(differences.mean()), "lower": float(np.quantile(bootstrap, 0.025)), "upper": float(np.quantile(bootstrap, 0.975))}


def _distribution_interval(left: Sequence[Mapping[str, Any]], right: Sequence[Mapping[str, Any]], draws: int = 10_000) -> dict[str, float]:
    left_values = np.asarray([row["behavioral_accuracy"] for row in left], dtype=float)
    right_values = np.asarray([row["behavioral_accuracy"] for row in right], dtype=float)
    rng = np.random.default_rng(7103)
    bootstrap = np.mean(rng.choice(left_values, size=(draws, len(left_values)), replace=True), axis=1) - np.mean(
        rng.choice(right_values, size=(draws, len(right_values)), replace=True), axis=1
    )
    return {"mean_difference": float(left_values.mean() - right_values.mean()), "lower": float(np.quantile(bootstrap, 0.025)), "upper": float(np.quantile(bootstrap, 0.975))}


def _confirmation_needs_extra_seeds(
    root: Path,
    sockets: Mapping[str, Mapping[str, Any]],
    best_single: Mapping[str, Any],
    random_distribution: Sequence[Mapping[str, Any]],
    seeds: Sequence[int],
    config: Mapping[str, Any],
) -> bool:
    def rows(identifier: str) -> list[dict[str, Any]]:
        return [_read_json(root / "jobs" / identifier / f"seed-{seed}" / "latest_result.json") for seed in seeds]

    searched = rows(sockets["rl_searched_socket"]["socket_id"])
    uniform = rows(sockets["uniform_lora"]["socket_id"])
    best_single_rows = rows(best_single["socket_id"])
    h1 = _paired_interval(searched, uniform)
    h2 = _paired_interval(searched, best_single_rows)
    random_rows = [
        _read_json(root / "jobs" / socket["socket_id"] / f"seed-{seed}" / "latest_result.json")
        for socket in random_distribution for seed in seeds
    ]
    guided_rows = [
        _read_json(path)
        for candidate_dir in (root / "jobs").glob("guided-*")
        for seed in config["search"]["seeds"]
        for path in [candidate_dir / f"seed-{seed}" / "latest_result.json"]
        if path.is_file()
    ]
    h3 = _distribution_interval(guided_rows, random_rows)
    margin = float(config["confirmation"]["noninferiority_margin"])
    return (
        h1["lower"] <= 0 <= h1["upper"]
        or h2["lower"] <= -margin <= h2["upper"]
        or h3["lower"] <= 0 <= h3["upper"]
    )


def _cached_completion(model: nn.Module, prompt: Sequence[int], sampler: StatefulSampler, *, max_tokens: int, temperature: float, eos_tokens: set[int]) -> list[int]:
    from mlx_lm.generate import generate_step

    def sample(logprobs: mx.array) -> mx.array:
        values = np.asarray(logprobs.astype(mx.float32))
        if values.ndim == 1:
            return mx.array(sampler.sample(values, temperature), dtype=mx.int32)
        return mx.array([sampler.sample(row, temperature) for row in values], dtype=mx.int32)

    generated = []
    for token, _ in generate_step(mx.array(prompt), model, max_tokens=max_tokens, sampler=sample):
        value = int(token)
        generated.append(value)
        if value in eos_tokens:
            break
    return generated or [next(iter(eos_tokens))]


def _batch_cached_completions(
    model: nn.Module,
    prompt: Sequence[int],
    sampler: StatefulSampler,
    *,
    group_size: int,
    max_tokens: int,
    temperature: float,
    eos_tokens: set[int],
    additional_stop_sequences: Sequence[Sequence[int]] = (),
    tokenizer: Any | None = None,
    stop_on_first_integer: bool = False,
) -> list[list[int]]:
    from mlx_lm.generate import BatchGenerator

    def sample(logprobs: mx.array) -> mx.array:
        values = np.asarray(logprobs.astype(mx.float32))
        if values.ndim == 1:
            return mx.array(sampler.sample(values, temperature), dtype=mx.int32)
        return mx.array([sampler.sample(row, temperature) for row in values], dtype=mx.int32)

    generator = BatchGenerator(
        model,
        stop_tokens=[[token] for token in eos_tokens] + [list(sequence) for sequence in additional_stop_sequences],
        sampler=sample,
        completion_batch_size=group_size,
        prefill_batch_size=group_size,
    )
    identifiers = generator.insert([list(prompt)] * group_size, [max_tokens] * group_size)
    results = {identifier: [] for identifier in identifiers}
    try:
        while responses := generator.next_generated():
            custom_stops = []
            for response in responses:
                if response.token is not None:
                    results[response.uid].append(int(response.token))
                if (
                    stop_on_first_integer
                    and tokenizer is not None
                    and response.finish_reason is None
                    and re.search(r"(?<![\d.])-?\d+(?![\d.])", tokenizer.decode(results[response.uid]))
                ):
                    custom_stops.append(response.uid)
            if custom_stops:
                generator.remove(custom_stops)
    finally:
        generator.close()
    fallback = next(iter(eos_tokens))
    return [results[identifier] or [fallback] for identifier in identifiers]


def _pad_rollouts(prompt: Sequence[int], completions: Sequence[Sequence[int]], pad_token: int) -> tuple[mx.array, mx.array, mx.array]:
    rows = [list(prompt) + list(completion) for completion in completions]
    maximum = max(len(row) for row in rows)
    padded = [row + [pad_token] * (maximum - len(row)) for row in rows]
    return (
        mx.array(padded, dtype=mx.int32),
        mx.array([len(prompt)] * len(rows), dtype=mx.int32),
        mx.array([len(completion) for completion in completions], dtype=mx.int32),
    )


def _training_reward(task: str, output: str, row: Mapping[str, Any], length: int, hard_limit: int) -> float:
    behavioral = (
        _arithmetic_behavioral_reward(output, row["answer"])
        if task == "arithmetic"
        else _python_public_test_fraction(output, row["completion"])
    )
    if behavioral <= 0:
        return 0.0
    format_bonus = 0.01 if (task == "arithmetic" and output.strip().lstrip("-").isdigit()) or (task == "python" and "def " in output) else 0.0
    length_bonus = 0.005 * max(0.0, 1.0 - length / max(1, hard_limit))
    return behavioral + min(0.01, format_bonus) + min(0.005, length_bonus)


def _arithmetic_behavioral_reward(output: str, expected: str) -> float:
    match = re.search(r"(?<![\d.])-?\d+(?![\d.])", output.strip())
    if not match:
        return 0.0
    observed = int(match.group(0))
    target = int(expected)
    if observed == target:
        return 1.0
    relative_error = abs(observed - target) / max(1.0, abs(target))
    return 0.25 * math.exp(-4.0 * relative_error)


def _python_public_test_fraction(output: str, expected: str, *, hidden: bool = False) -> float:
    code = _extract_python(output)
    try:
        tree = ast.parse(code)
        expected_tree = ast.parse(expected)
    except SyntaxError:
        return 0.0
    allowed = (
        ast.Module, ast.FunctionDef, ast.arguments, ast.arg, ast.Return, ast.Name,
        ast.Load, ast.BinOp, ast.Add, ast.Sub, ast.Mult, ast.Mod, ast.Subscript,
        ast.Constant, ast.UnaryOp, ast.USub, ast.Call, ast.Compare, ast.Eq,
        ast.In, ast.NotIn,
    )
    if any(not isinstance(node, allowed) for node in ast.walk(tree)):
        return 0.0
    for node in ast.walk(tree):
        if isinstance(node, ast.Call) and (not isinstance(node.func, ast.Name) or node.func.id not in {"min", "max"}):
            return 0.0
    expected_functions = [node.name for node in expected_tree.body if isinstance(node, ast.FunctionDef)]
    candidate_functions = [node.name for node in tree.body if isinstance(node, ast.FunctionDef)]
    if len(expected_functions) != 1 or candidate_functions != expected_functions:
        return 0.0
    function = expected_functions[0]
    public_cases = {
        "clamp": [((5, 0, 3), 3), ((-2, 0, 3), 0), ((2, 0, 3), 2)],
        "is_even": [((4,), True), ((3,), False), ((0,), True)],
        "last_item": [(([1, 2, 3],), 3), ((["x"],), "x"), (([-1, 7],), 7)],
        "add_tax": [((100, 0.1), 110.0), ((20, 0.0), 20.0), ((50, 0.2), 60.0)],
        "contains": [(([1, 2], 2), True), (([1, 2], 3), False), (([], 1), False)],
        "square": [((3,), 9), ((-4,), 16), ((0,), 0)],
        "first_item": [(([4, 5, 6],), 4), ((["x"],), "x"), (([-2, 8],), -2)],
        "subtract": [((5, 2), 3), ((-1, 4), -5), ((0, -3), 3)],
    }
    cases = public_cases.get(function)
    if cases is None:
        return 0.0
    selected = cases[1:] if hidden else cases[:2]
    assertions = "\n".join(
        f"\ntry:\n    passed += int({function}(*{args!r}) == {value!r})\nexcept Exception:\n    pass"
        for args, value in selected
    )
    program = f"{code}\npassed = 0\n{assertions}\nprint(passed)\n"
    try:
        result = subprocess.run(
            [sys.executable, "-I", "-S", "-c", program],
            stdin=subprocess.DEVNULL,
            stdout=subprocess.PIPE,
            stderr=subprocess.DEVNULL,
            timeout=1.0,
            check=False,
            text=True,
            env={"PATH": "/usr/bin:/bin"},
        )
    except (subprocess.TimeoutExpired, OSError):
        return 0.0
    try:
        passed = int(result.stdout.strip()) if result.returncode == 0 else 0
    except ValueError:
        return 0.0
    return passed / len(selected)


def _evaluate_candidate(model: nn.Module, tokenizer: Any, data_root: Path, *, limit: int, split: str) -> dict[str, Any]:
    from mlx_lm import batch_generate
    from mlx_lm.sample_utils import make_sampler

    result: dict[str, Any] = {}
    for task in ("arithmetic", "python"):
        rows = _read_jsonl(data_root / task / f"{split}.jsonl")[:limit]
        prompts = [_prompt_tokens(tokenizer, row["prompt"]) for row in rows]
        response = batch_generate(model, tokenizer, prompts, max_tokens=64, sampler=make_sampler(temp=0.0), verbose=False)
        outcomes = [
            _arithmetic_correct(output, row["answer"])
            if task == "arithmetic"
            else _python_public_test_fraction(output, row["completion"], hidden=True) == 1.0
            for output, row in zip(response.texts, rows)
        ]
        result[task] = {"accuracy": float(np.mean(outcomes)), "outcomes": outcomes, "rows": len(rows)}
    result["behavioral_accuracy"] = 0.5625 * result["arithmetic"]["accuracy"] + 0.4375 * result["python"]["accuracy"]
    return result


def _prompt_scores(result: Mapping[str, Any]) -> np.ndarray:
    arithmetic = np.asarray(result["behavior"]["arithmetic"]["outcomes"], dtype=float) * 1.125
    python = np.asarray(result["behavior"]["python"]["outcomes"], dtype=float) * 0.875
    return np.concatenate([arithmetic, python])


def _summarize_job(job: Mapping[str, Any], health: Sequence[Mapping[str, Any]], rewards: Sequence[float], generated_tokens: int, behavior: Mapping[str, Any], elapsed: float) -> dict[str, Any]:
    recent = list(health[-100:])
    mean_reward = float(np.mean(rewards)) if rewards else 0.0
    reward_per_1k = 1000.0 * sum(rewards) / max(1, generated_tokens)
    retention = max(0.0, 1.0 - float(np.mean([row["kl_divergence"] for row in recent])) / max(1e-8, float(job["health"]["kl_threshold"])))
    stability = 1.0
    behavioral_improvement = float(behavior["behavioral_accuracy"]) - float(job.get("base_behavioral_accuracy", 0.0))
    behavioral_improvement_per_1k = 1000.0 * behavioral_improvement / max(1, generated_tokens)
    efficiency = min(1.0, max(0.0, behavioral_improvement_per_1k) / float(job.get("selection", {}).get("efficiency_scale", 0.02)))
    score = 0.45 * behavior["arithmetic"]["accuracy"] + 0.35 * behavior["python"]["accuracy"] + 0.10 * efficiency + 0.05 * retention + 0.05 * stability
    task_families = sorted({str(row.get("task_family", row.get("task", "unknown"))) for row in recent})
    reward_by_task_family = {
        family: float(np.mean([
            row["mean_task_reward"]
            for row in recent
            if str(row.get("task_family", row.get("task", "unknown"))) == family
        ]))
        for family in task_families
    }
    entry_keys = sorted({key for row in recent for key in row.get("gradient_norm_by_entry", {})})
    gradient_norm_by_entry = {
        key: float(np.mean([row["gradient_norm_by_entry"][key] for row in recent if key in row.get("gradient_norm_by_entry", {})]))
        for key in entry_keys
    }
    body = {
        "schema": f"{SCHEMA}.candidate_result.v1",
        "candidate_id": job["candidate"]["socket_id"],
        "socket_hash": job["candidate"]["socket_hash"],
        "arm": job["candidate"]["arm"],
        "method": job["method"],
        "seed": job["seed"],
        "updates": int(health[-1]["update"]) if health else 0,
        "target_updates": job["target_update"],
        "generated_tokens": generated_tokens,
        "mean_task_reward": mean_reward,
        "reward_by_task_family": reward_by_task_family,
        "reward_per_1000_generated_tokens": reward_per_1k,
        "behavioral_improvement": behavioral_improvement,
        "behavioral_improvement_per_1000_generated_tokens": behavioral_improvement_per_1k,
        "behavioral_accuracy": behavior["behavioral_accuracy"],
        "behavior": behavior,
        "retention_score": retention,
        "seed_stability": stability,
        "selection_score": score,
        "zero_variance_group_fraction": float(np.mean([row["zero_variance_group"] for row in recent])),
        "completion_diversity": float(np.mean([row["completion_diversity"] for row in recent])),
        "policy_entropy": float(np.mean([row["policy_entropy"] for row in recent])),
        "kl_divergence": float(np.mean([row["kl_divergence"] for row in recent])),
        "clip_fraction": float(np.mean([row["clip_fraction"] for row in recent])),
        "gradient_norm": float(np.mean([row["gradient_norm"] for row in recent])),
        "gradient_norm_by_entry": gradient_norm_by_entry,
        "adapter_update_norm": float(np.mean([row["adapter_update_norm"] for row in recent])),
        "unhealthy": (
            any(row["unhealthy"] for row in recent)
            or float(np.mean([row["zero_variance_group"] for row in recent]))
            > float(job["health"]["zero_variance_group_threshold"])
        ),
        "wall_clock_seconds": elapsed,
        "peak_memory_bytes": max(int(row["peak_memory_bytes"]) for row in recent),
        "health_tail": recent,
    }
    return {**body, "result_hash": _hash(body)}


