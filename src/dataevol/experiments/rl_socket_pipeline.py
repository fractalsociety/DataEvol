def summarize_uniform_kl_sweep(
    *,
    audit: Mapping[str, Any],
    frozen: Mapping[str, Any],
    baseline: Mapping[str, Any],
    protected: Sequence[str],
    schedules: Sequence[Mapping[str, Any]],
    results: Mapping[str, Sequence[Mapping[str, Any]]],
    bootstrap_draws: int,
) -> dict[str, Any]:
    baseline_arithmetic = baseline["test"]["arithmetic"]
    schedule_reports: dict[str, Any] = {}
    for schedule in schedules:
        schedule_id = str(schedule["id"])
        rows = results[schedule_id]
        interval = _arithmetic_paired_interval(rows, baseline_arithmetic, draws=bootstrap_draws)
        arithmetic = [float(row["behavior"]["arithmetic"]["accuracy"]) for row in rows]
        python = [float(row["behavior"]["python"]["accuracy"]) for row in rows]
        healthy = all(not row["unhealthy"] and not row["aborted_early"] for row in rows)
        eligible = healthy and min(python) >= 0.99 and interval["lower"] > 0
        schedule_reports[schedule_id] = {
            "config": dict(schedule),
            "seeds": [int(row["seed"]) for row in rows],
            "updates": [int(row["updates"]) for row in rows],
            "arithmetic_accuracy_by_seed": arithmetic,
            "mean_arithmetic_accuracy": float(np.mean(arithmetic)),
            "mean_gain_over_sft": float(np.mean(arithmetic)) - float(baseline_arithmetic["accuracy"]),
            "python_retention_by_seed": python,
            "mean_kl_by_seed": [float(row["kl_divergence"]) for row in rows],
            "abort_reasons": [row.get("abort_reason") for row in rows],
            "paired_interval": interval,
            "healthy": healthy,
            "eligible": eligible,
            "result_hashes": [row["result_hash"] for row in rows],
        }
    eligible = [
        (identifier, report)
        for identifier, report in schedule_reports.items()
        if report["eligible"]
    ]
    winner = max(eligible, key=lambda item: item[1]["mean_arithmetic_accuracy"])[0] if eligible else None
    body = {
        "schema": f"{SCHEMA}.uniform_kl_sweep_result.v1",
        "audit_hash": audit["audit_hash"],
        "frozen_behavior": frozen["test"],
        "uniform_sft_baseline": baseline,
        "protected_python_entries": list(protected),
        "schedules": schedule_reports,
        "winning_schedule": winner,
        "verdict": "ELIGIBLE_UNIFORM_RL_SCHEDULE" if winner else "NO_ELIGIBLE_UNIFORM_RL_SCHEDULE",
        "completed_at": utc_now(),
    }
    return {**body, "result_hash": _hash(body)}


def _arithmetic_paired_interval(
    rows: Sequence[Mapping[str, Any]],
    baseline: Mapping[str, Any],
    *,
    draws: int,
) -> dict[str, float]:
    baseline_outcomes = np.asarray(baseline["outcomes"], dtype=float)
    differences = np.concatenate([
        np.asarray(row["behavior"]["arithmetic"]["outcomes"], dtype=float) - baseline_outcomes
        for row in rows
    ])
    rng = np.random.default_rng(7319)
    bootstrap = np.mean(
        rng.choice(differences, size=(draws, len(differences)), replace=True),
        axis=1,
    )
    return {
        "mean_difference": float(np.mean(differences)),
        "lower": float(np.quantile(bootstrap, 0.025)),
        "upper": float(np.quantile(bootstrap, 0.975)),
    }


def train_candidate_job(job_path: str | Path) -> dict[str, Any]:
    global TERMINATE_REQUESTED
    job = _read_json(Path(job_path))
    root = Path(job["run_dir"])
    candidate_dir = root / "jobs" / job["candidate"]["socket_id"] / f"seed-{job['seed']}"
    candidate_dir.mkdir(parents=True, exist_ok=True)
    metrics_path = candidate_dir / f"metrics-{job['target_update']:08d}.json"
    if metrics_path.is_file():
        return _read_json(metrics_path)
    signal.signal(signal.SIGINT, _request_termination)
    signal.signal(signal.SIGTERM, _request_termination)
    mx.random.seed(int(job["seed"]))
    random.seed(int(job["seed"]))
    np.random.seed(int(job["seed"]))
    from mlx_lm import load

    model, tokenizer = load(job["model_path"])
    _apply_socket(
        model,
        job["candidate"],
        scale=float(job["adapter_contract"]["lora_scale"]),
        dropout=float(job["adapter_contract"]["dropout"]),
    )
    reference, _ = load(job["model_path"])
    _apply_socket(
        reference,
        job["candidate"],
        scale=float(job["adapter_contract"]["lora_scale"]),
        dropout=float(job["adapter_contract"]["dropout"]),
    )
    starting_adapter = job.get("starting_adapter")
    if starting_adapter:
        weights = list(mx.load(starting_adapter).items())
        model.load_weights(weights, strict=False)
        reference.load_weights(weights, strict=False)
    else:
        reference.load_weights(list(tree_flatten(model.trainable_parameters())), strict=False)
    reference.freeze()
    mx.eval(model.parameters(), reference.parameters())
    ppo_config = PPOConfig(**job["ppo"])
    optimizer = optim.AdamW(
        learning_rate=ppo_config.learning_rate,
        weight_decay=ppo_config.weight_decay,
        betas=job["optimizer"]["betas"],
        eps=job["optimizer"]["eps"],
    )
    sampler = StatefulSampler(int(job["sampler_seed"]))
    update = 0
    reward_history: list[float] = []
    health_history: list[dict[str, Any]] = []
    generated_tokens = 0
    current_beta = ppo_config.beta
    abort_reason: str | None = None
    latest = _latest_checkpoint(candidate_dir / "checkpoints")
    if latest:
        loaded = load_checkpoint(latest, model=model, optimizer=optimizer, sampler=sampler)
        state = loaded["training_state"]
        update = int(state["update"])
        reward_history = list(state["reward_history"])
        health_history = list(state["health_history"])
        generated_tokens = int(state["generated_tokens"])
        current_beta = float(state.get("current_beta", current_beta))
    data_root = root / "datasets"
    datasets = {
        task: _read_jsonl(data_root / task / "train.jsonl")
        for task in ("arithmetic", "python")
    }
    started = time.perf_counter()
    task_schedule = [
        task
        for task, frequency in job.get("task_mix", {"arithmetic": 1, "python": 1}).items()
        for _ in range(int(frequency))
    ]
    if not task_schedule or any(task not in datasets for task in task_schedule):
        raise ValueError("task_mix must contain positive arithmetic/Python frequencies")
    while update < int(job["target_update"]):
        task = task_schedule[update % len(task_schedule)]
        row = random.choice(datasets[task])
        prompt_tokens = _prompt_tokens(tokenizer, row["prompt"])
        task_max_tokens = int(
            job["rollout"].get("max_completion_tokens_by_task", {}).get(
                task, job["rollout"]["max_completion_tokens"]
            )
        )
        stop_sequences = [[13]] if task == "arithmetic" else [[13, 13]]
        completions = _batch_cached_completions(
            model,
            prompt_tokens,
            sampler,
            group_size=int(job["rollout"]["group_size"]),
            max_tokens=task_max_tokens,
            temperature=float(job["rollout"]["temperature"]),
            eos_tokens=set(tokenizer.eos_token_ids),
            additional_stop_sequences=stop_sequences,
            tokenizer=tokenizer,
            stop_on_first_integer=task == "arithmetic",
        )
        decoded = [tokenizer.decode(tokens) for tokens in completions]
        rewards = [
            _training_reward(task, output, row, len(tokens), task_max_tokens)
            for output, tokens in zip(decoded, completions)
        ]
        if job.get("shuffle_rewards"):
            reward_rng = random.Random(int(job["seed"]) * 1_000_003 + update)
            reward_rng.shuffle(rewards)
        reward_history.extend(rewards)
        generated_tokens += sum(len(tokens) for tokens in completions)
        tokens, prompt_lengths, completion_lengths = _pad_rollouts(prompt_tokens, completions, tokenizer.eos_token_id or 0)
        batch = make_rollout_batch(
            model,
            reference,
            tokens,
            prompt_lengths,
            completion_lengths,
            mx.array(rewards, dtype=mx.float32),
        )
        before = {name: mx.array(value) for name, value in tree_flatten(model.trainable_parameters())}

        epoch_gradient_norms = []
        epoch_entry_gradient_norms: list[dict[str, float]] = []
        for _ in range(int(job.get("ppo_epochs", 1))):
            runtime_ppo = replace(ppo_config, beta=current_beta)

            def objective() -> tuple[mx.array, dict[str, mx.array]]:
                return ppo_loss(model(tokens), batch, runtime_ppo)

            loss_and_grad = nn.value_and_grad(model, objective)
            (loss, metrics), gradients = loss_and_grad()
            mx.eval(loss, *metrics.values(), *[value for _, value in tree_flatten(gradients)], *before.values())
            gradients, gradient_norm = clip_gradients(gradients, ppo_config.max_grad_norm)
            epoch_entry_gradient_norms.append(gradient_norms_by_entry(gradients, job["candidate"]))
            gradients = mask_frozen_entry_gradients(
                gradients,
                job["candidate"],
                set(job.get("frozen_entries", [])),
            )
            finite = all(bool(mx.all(mx.isfinite(value))) for _, value in tree_flatten(gradients))
            if not finite:
                health_history.append({"update": update, "unhealthy": True, "reason": "non-finite gradient"})
                _checkpoint_job(
                    candidate_dir, job, model, optimizer, sampler, update,
                    reward_history, health_history, generated_tokens, current_beta,
                )
                raise FloatingPointError("non-finite gradient")
            optimizer.update(model, gradients)
            mx.eval(model.parameters(), optimizer.state, loss, *metrics.values(), gradient_norm)
            epoch_gradient_norms.append(float(gradient_norm))
        update_norm = math.sqrt(sum(float(mx.sum((value - before[name]) ** 2)) for name, value in tree_flatten(model.trainable_parameters())))
        entropy = _policy_entropy(model(tokens), batch)
        reward_array = np.asarray(rewards, dtype=float)
        zero_variance = float(np.var(reward_array) < 1e-12)
        health = {
            "update": update + 1,
            "task": task,
            "task_family": row.get("family", task),
            "mean_task_reward": float(reward_array.mean()),
            "pass_at_1": float(reward_array[0] >= 1.0),
            "pass_at_group_size": float(np.any(reward_array >= 1.0)),
            "generated_tokens": sum(len(item) for item in completions),
            "reward_per_1000_generated_tokens": 1000.0 * float(reward_array.sum()) / max(1, sum(len(item) for item in completions)),
            "zero_variance_group": zero_variance,
            "reward_entropy": _binary_entropy(float(np.mean(reward_array >= 1.0))),
            "nonzero_advantage_fraction": float(np.mean(np.abs(reward_array - reward_array.mean()) > 1e-8)),
            "completion_diversity": len({tuple(item) for item in completions}) / len(completions),
            "policy_entropy": entropy,
            "kl_divergence": float(metrics["kl"]),
            "clip_fraction": float(metrics["clip_fraction"]),
            "gradient_norm": float(np.mean(epoch_gradient_norms)),
            "gradient_norm_by_entry": {
                key: float(np.mean([values[key] for values in epoch_entry_gradient_norms]))
                for key in epoch_entry_gradient_norms[0]
            },
            "adapter_update_norm": update_norm,
            "loss": float(loss),
            "hard_limit_fraction": float(np.mean([len(item) >= task_max_tokens for item in completions])),
            "peak_memory_bytes": int(mx.get_peak_memory()),
            "kl_beta": current_beta,
        }
        if health["kl_divergence"] > float(job["health"]["kl_threshold"]):
            current_beta = min(
                float(job["health"].get("kl_beta_max", 2.0)),
                current_beta * float(job["health"].get("kl_beta_growth", 2.0)),
            )
        elif health["kl_divergence"] < float(job["health"]["kl_threshold"]) / 2:
            current_beta = max(
                ppo_config.beta,
                current_beta / float(job["health"].get("kl_beta_growth", 2.0)),
            )
        health["next_kl_beta"] = current_beta
        health["unhealthy"] = _is_unhealthy(health, job["health"])
        evaluation_every = int(job.get("evaluation_every", 0))
        if evaluation_every and (update + 1) % evaluation_every == 0:
            health["heldout_behavior"] = _evaluate_candidate(
                model,
                tokenizer,
                data_root,
                limit=int(job["evaluation"]["limit"]),
                split=str(job.get("monitoring_split", job["evaluation"]["split"])),
            )
        health_history.append(health)
        update += 1
        abort_reason = _health_abort_reason(health_history, job["health"])
        if update % int(job["checkpoint_every"]) == 0 or update == int(job["target_update"]) or TERMINATE_REQUESTED:
            _checkpoint_job(
                candidate_dir, job, model, optimizer, sampler, update,
                reward_history, health_history, generated_tokens, current_beta,
            )
        if abort_reason:
            if update % int(job["checkpoint_every"]) != 0:
                _checkpoint_job(
                    candidate_dir, job, model, optimizer, sampler, update,
                    reward_history, health_history, generated_tokens, current_beta,
                )
            break
        if TERMINATE_REQUESTED:
            raise SystemExit(128 + signal.SIGTERM)
    behavior = _evaluate_candidate(
        model,
        tokenizer,
        data_root,
        limit=int(job["evaluation"]["limit"]),
        split=str(job["evaluation"]["split"]),
    )
    summary = _summarize_job(job, health_history, reward_history, generated_tokens, behavior, time.perf_counter() - started)
    summary["aborted_early"] = abort_reason is not None
    summary["abort_reason"] = abort_reason
    summary["unhealthy"] = bool(summary["unhealthy"] or abort_reason)
    summary["kl_reference_kind"] = job.get("kl_reference_kind", "base_or_initial_policy")
    summary["kl_reference_adapter_sha256"] = (
        _file_hash(Path(starting_adapter)) if starting_adapter else None
    )
    summary["frozen_entries"] = list(job.get("frozen_entries", []))
    summary["task_mix"] = dict(job.get("task_mix", {"arithmetic": 1, "python": 1}))
    summary["heldout_evaluation_curve"] = [
        {"update": row["update"], **row["heldout_behavior"]}
        for row in health_history
        if "heldout_behavior" in row
    ]
    summary["monitoring_split"] = job.get("monitoring_split", job["evaluation"]["split"])
    prior = [_read_json(path) for path in sorted(candidate_dir.glob("metrics-*.json"))]
    curve = sorted(
        [{"generated_tokens": row["generated_tokens"], "behavioral_accuracy": row["behavioral_accuracy"]} for row in prior]
        + [{"generated_tokens": summary["generated_tokens"], "behavioral_accuracy": summary["behavioral_accuracy"]}],
        key=lambda row: row["generated_tokens"],
    )
    summary["accuracy_vs_generated_tokens"] = curve
    summary["behavioral_auc_generated_tokens"] = _curve_auc(curve)
    if prior and summary["mean_task_reward"] > prior[-1]["mean_task_reward"] and summary["behavioral_accuracy"] < prior[-1]["behavioral_accuracy"]:
        summary["unhealthy"] = True
        summary["health_failure"] = "reward increased while held-out behavior decreased"
    body = {key: value for key, value in summary.items() if key != "result_hash"}
    summary["result_hash"] = _hash(body)
    _write_atomic_json(metrics_path, summary)
    _write_atomic_json(candidate_dir / "latest_result.json", summary)
    return summary


