def run_correctness_audit(run_dir: str | Path, config_path: str | Path = "configs/rl_socket_v2.yaml") -> dict[str, Any]:
    root = Path(run_dir)
    root.mkdir(parents=True, exist_ok=True)
    report_path = root / "trainer_audit.json"
    config = _read_json(Path(config_path))
    command = [sys.executable, "-m", "pytest", "-q", "tests/test_rl_socket_trainer.py"]
    started = time.perf_counter()
    result = subprocess.run(command, cwd=_repo_root(), text=True, capture_output=True, check=False)
    positive_control = _real_uniform_lora_positive_control(config["model"]["path"], root / "trainer_audit_positive_control")
    source_paths = [
        Path(__file__),
        Path(__file__).with_name("rl_socket_trainer.py"),
        _repo_root() / "tests/test_rl_socket_trainer.py",
        Path(config_path),
    ]
    source_hashes = {str(path): _file_hash(path) for path in source_paths}
    eligible = result.returncode == 0 and bool(positive_control["passed"])
    body = {
        "schema": f"{SCHEMA}.trainer_audit.v1",
        "git_commit": _git_commit(),
        "command": command,
        "returncode": result.returncode,
        "elapsed_seconds": time.perf_counter() - started,
        "stdout": result.stdout[-8_000:],
        "stderr": result.stderr[-8_000:],
        "source_hashes": source_hashes,
        "real_uniform_lora_positive_control": positive_control,
        "checks": [
            "prompt_conditioned_policy_loss",
            "prompt_and_padding_masking",
            "old_policy_reference_separation",
            "kl_behavior",
            "gradient_direction",
            "sampling_diversity_and_rng_resume",
            "uniform_lora_positive_control",
            "resume_equivalence_100_vs_50_plus_50",
        ],
        "verdict": "ELIGIBLE" if eligible else "REJECTED",
        "completed_at": utc_now(),
    }
    report = {**body, "audit_hash": _hash(body)}
    _write_atomic_json(report_path, report)
    return report


def build_candidate_manifest(config: Mapping[str, Any], run_dir: Path) -> dict[str, Any]:
    path = run_dir / "candidate_manifest.json"
    if path.is_file():
        existing = _read_json(path)
        if existing.get("generator_source_hash") != _file_hash(Path(__file__)):
            raise ValueError("candidate manifest was generated by different pipeline source")
        return existing
    count = int(config["search"]["candidates_per_arm"])
    source = Path(config["sft_source_experiment"])
    proxy_report = _read_json(source / "discovery_search/search_report.json")
    guided = []
    for index, row in enumerate(proxy_report["ranked_candidates"][:count]):
        socket = dict(row["socket"])
        socket["socket_id"] = f"guided-{index:02d}"
        socket["generation"] = f"guided-from-sft:{socket['generation']}"
        socket = _rehash_socket(socket)
        guided.append(_tag_topology(socket, arm="guided_search"))
    if len(guided) < count:
        raise ValueError("SFT source does not contain enough guided candidates")
    random_candidates = generate_socket_candidates(count=count, seed=int(config["search"]["random_seed"]))
    depth_layouts = (
        (0, 2, 4, 6, 9, 12),
        (4, 8, 10, 12, 14, 16),
        (8, 12, 15, 17, 19, 21),
    )
    random_arm = []
    for index, socket in enumerate(random_candidates):
        candidate = dict(socket)
        if index < len(depth_layouts):
            candidate["entries"] = [
                {**entry, "layer": layer}
                for entry, layer in zip(candidate["entries"], depth_layouts[index])
            ]
        candidate["socket_id"] = f"random-search-{index:02d}"
        candidate["generation"] = f"balanced-random:{candidate['generation']}"
        candidate = _rehash_socket(candidate)
        random_arm.append(_tag_topology(candidate, arm="random_search"))
    all_candidates = guided + random_arm
    target = int(config["adapter"]["target_parameters"])
    for candidate in all_candidates:
        mismatch = abs(int(candidate["trainable_parameters"]) - target) / target
        if mismatch > 0.005:
            raise ValueError(f"candidate parameter mismatch exceeds 0.5%: {candidate['socket_id']}={mismatch}")
    body = {
        "schema": f"{SCHEMA}.candidate_manifest.v1",
        "guided_count": len(guided),
        "random_count": len(random_arm),
        "target_parameters": target,
        "maximum_parameter_mismatch": 0.005,
        "candidates": all_candidates,
        "generator_source_hash": _file_hash(Path(__file__)),
    }
    manifest = {**body, "manifest_hash": _hash(body)}
    _write_atomic_json(path, manifest)
    return manifest


def run_pipeline(config_path: str | Path, run_dir: str | Path, *, resume: bool) -> dict[str, Any]:
    config_path = Path(config_path)
    root = Path(run_dir)
    root.mkdir(parents=True, exist_ok=True)
    with (root / "pipeline.lock").open("a+") as lock:
        try:
            fcntl.flock(lock.fileno(), fcntl.LOCK_EX | fcntl.LOCK_NB)
        except BlockingIOError as error:
            raise RuntimeError(f"RL-socket pipeline is already running: {root}") from error
        config = _read_json(config_path)
        _validate_config(config)
        audit = run_correctness_audit(root, config_path)
        if audit["verdict"] != "ELIGIBLE":
            _write_failure(root, "trainer correctness audit failed", audit["stderr"] or audit["stdout"])
            _update_handoff(root, config, stage="trainer_audit", current=None, next_command="fix trainer audit")
            return {"verdict": "E_TRAINER_FAILED", "audit": audit}
        manifest = build_candidate_manifest(config, root)
        prepare_datasets(root / "datasets")
        _evaluate_frozen_base(config, root)
        if not (root / "failure_summary.json").is_file():
            _write_atomic_json(root / "failure_summary.json", {"schema": f"{SCHEMA}.failures.v1", "failures": []})
        _update_handoff(root, config, stage="search_pending", current=None, next_command="run stage_a candidate jobs")
        stages = [("stage_a", 100), ("stage_b", 300), ("stage_c", 700)]
        active = [candidate["socket_id"] for candidate in manifest["candidates"]]
        for stage, target_update in stages:
            active_candidates = [candidate for candidate in manifest["candidates"] if candidate["socket_id"] in active]
            for candidate in active_candidates:
                for seed in config["search"]["seeds"]:
                    _run_candidate_subprocess(config_path, root, candidate, int(seed), target_update, stage)
                    _update_handoff(
                        root,
                        config,
                        stage=stage,
                        current=candidate["socket_id"],
                        next_command=f"resume {stage} after {candidate['socket_id']}",
                    )
            active = _promote_candidates(root, active_candidates, config, stage)
            _write_atomic_json(root / f"{stage}_promotion.json", {"stage": stage, "target_update": target_update, "retained": active})
        result = _run_confirmation(config_path, root, config, manifest, active)
        _update_handoff(root, config, stage="complete", current=None, next_command="review hypothesis_verdicts.json")
        return result


def train_candidate_job(job_path: str | Path) -> dict[str, Any]:
    global TERMINATE_REQUESTED
    job = _read_json(Path(job_path))
    root = Path(job["run_dir"])
    candidate_dir = root / "jobs" / job["candidate"]["socket_id"] / f"seed-{job['seed']}"
    candidate_dir.mkdir(parents=True, exist_ok=True)
    metrics_path = candidate_dir / f"metrics-{job['target_update']:08d}.json"
    if metrics_path.is_file():
        return _read_json(metrics_path)
    signal.signal(signal.SIGINT, _request_termination)
    signal.signal(signal.SIGTERM, _request_termination)
    mx.random.seed(int(job["seed"]))
    random.seed(int(job["seed"]))
    np.random.seed(int(job["seed"]))
    from mlx_lm import load

    model, tokenizer = load(job["model_path"])
    _apply_socket(model, job["candidate"])
    reference, _ = load(job["model_path"])
    _apply_socket(reference, job["candidate"])
    starting_adapter = job.get("starting_adapter")
    if starting_adapter:
        weights = list(mx.load(starting_adapter).items())
        model.load_weights(weights, strict=False)
        reference.load_weights(weights, strict=False)
    else:
        reference.load_weights(list(tree_flatten(model.trainable_parameters())), strict=False)
    reference.freeze()
    mx.eval(model.parameters(), reference.parameters())
    ppo_config = PPOConfig(**job["ppo"])
    optimizer = optim.AdamW(
        learning_rate=ppo_config.learning_rate,
        weight_decay=ppo_config.weight_decay,
        betas=job["optimizer"]["betas"],
        eps=job["optimizer"]["eps"],
    )
    sampler = StatefulSampler(int(job["sampler_seed"]))
    update = 0
    reward_history: list[float] = []
    health_history: list[dict[str, Any]] = []
    generated_tokens = 0
    latest = _latest_checkpoint(candidate_dir / "checkpoints")
    if latest:
        loaded = load_checkpoint(latest, model=model, optimizer=optimizer, sampler=sampler)
        state = loaded["training_state"]
        update = int(state["update"])
        reward_history = list(state["reward_history"])
        health_history = list(state["health_history"])
        generated_tokens = int(state["generated_tokens"])
    data_root = root / "datasets"
    datasets = {
        task: _read_jsonl(data_root / task / "train.jsonl")
        for task in ("arithmetic", "python")
    }
    started = time.perf_counter()
    while update < int(job["target_update"]):
        task = ("arithmetic", "python")[update % 2]
        row = random.choice(datasets[task])
        prompt_tokens = _prompt_tokens(tokenizer, row["prompt"])
        completions = _batch_cached_completions(
            model,
            prompt_tokens,
            sampler,
            group_size=int(job["rollout"]["group_size"]),
            max_tokens=int(job["rollout"]["max_completion_tokens"]),
            temperature=float(job["rollout"]["temperature"]),
            eos_tokens=set(tokenizer.eos_token_ids),
        )
        decoded = [tokenizer.decode(tokens) for tokens in completions]
        rewards = [_training_reward(task, output, row, len(tokens), int(job["rollout"]["max_completion_tokens"])) for output, tokens in zip(decoded, completions)]
        if job.get("shuffle_rewards"):
            reward_rng = random.Random(int(job["seed"]) * 1_000_003 + update)
            reward_rng.shuffle(rewards)
        reward_history.extend(rewards)
        generated_tokens += sum(len(tokens) for tokens in completions)
        tokens, prompt_lengths, completion_lengths = _pad_rollouts(prompt_tokens, completions, tokenizer.eos_token_id or 0)
        batch = make_rollout_batch(
            model,
            reference,
            tokens,
            prompt_lengths,
            completion_lengths,
            mx.array(rewards, dtype=mx.float32),
        )
        before = {name: mx.array(value) for name, value in tree_flatten(model.trainable_parameters())}

        def objective() -> tuple[mx.array, dict[str, mx.array]]:
            return ppo_loss(model(tokens), batch, ppo_config)

        loss_and_grad = nn.value_and_grad(model, objective)
        (loss, metrics), gradients = loss_and_grad()
        mx.eval(loss, *metrics.values(), *[value for _, value in tree_flatten(gradients)], *before.values())
        gradients, gradient_norm = clip_gradients(gradients, ppo_config.max_grad_norm)
        finite = all(bool(mx.all(mx.isfinite(value))) for _, value in tree_flatten(gradients))
        if not finite:
            health_history.append({"update": update, "unhealthy": True, "reason": "non-finite gradient"})
            _checkpoint_job(candidate_dir, job, model, optimizer, sampler, update, reward_history, health_history, generated_tokens)
            raise FloatingPointError("non-finite gradient")
        optimizer.update(model, gradients)
        mx.eval(model.parameters(), optimizer.state, loss, *metrics.values(), gradient_norm)
        update_norm = math.sqrt(sum(float(mx.sum((value - before[name]) ** 2)) for name, value in tree_flatten(model.trainable_parameters())))
        entropy = _policy_entropy(model(tokens), batch)
        reward_array = np.asarray(rewards, dtype=float)
        zero_variance = float(np.var(reward_array) < 1e-12)
        health = {
            "update": update + 1,
            "task": task,
            "mean_task_reward": float(reward_array.mean()),
            "pass_at_1": float(reward_array[0] >= 1.0),
            "pass_at_group_size": float(np.any(reward_array >= 1.0)),
            "generated_tokens": sum(len(item) for item in completions),
            "reward_per_1000_generated_tokens": 1000.0 * float(reward_array.sum()) / max(1, sum(len(item) for item in completions)),
            "zero_variance_group": zero_variance,
            "reward_entropy": _binary_entropy(float(np.mean(reward_array >= 1.0))),
            "nonzero_advantage_fraction": float(np.mean(np.abs(reward_array - reward_array.mean()) > 1e-8)),
            "completion_diversity": len({tuple(item) for item in completions}) / len(completions),
            "policy_entropy": entropy,
            "kl_divergence": float(metrics["kl"]),
            "clip_fraction": float(metrics["clip_fraction"]),
            "gradient_norm": float(gradient_norm),
            "adapter_update_norm": update_norm,
            "loss": float(loss),
            "hard_limit_fraction": float(np.mean([len(item) >= int(job["rollout"]["max_completion_tokens"]) for item in completions])),
            "peak_memory_bytes": int(mx.get_peak_memory()),
        }
        health["unhealthy"] = _is_unhealthy(health, job["health"])
        health_history.append(health)
        update += 1
        if update % int(job["checkpoint_every"]) == 0 or update == int(job["target_update"]) or TERMINATE_REQUESTED:
            _checkpoint_job(candidate_dir, job, model, optimizer, sampler, update, reward_history, health_history, generated_tokens)
        if TERMINATE_REQUESTED:
            raise SystemExit(128 + signal.SIGTERM)
    behavior = _evaluate_candidate(
        model,
        tokenizer,
        data_root,
        limit=int(job["evaluation"]["limit"]),
        split=str(job["evaluation"]["split"]),
    )
    summary = _summarize_job(job, health_history, reward_history, generated_tokens, behavior, time.perf_counter() - started)
    prior = [_read_json(path) for path in sorted(candidate_dir.glob("metrics-*.json"))]
    curve = sorted(
        [{"generated_tokens": row["generated_tokens"], "behavioral_accuracy": row["behavioral_accuracy"]} for row in prior]
        + [{"generated_tokens": summary["generated_tokens"], "behavioral_accuracy": summary["behavioral_accuracy"]}],
        key=lambda row: row["generated_tokens"],
    )
    summary["accuracy_vs_generated_tokens"] = curve
    summary["behavioral_auc_generated_tokens"] = _curve_auc(curve)
    if prior and summary["mean_task_reward"] > prior[-1]["mean_task_reward"] and summary["behavioral_accuracy"] < prior[-1]["behavioral_accuracy"]:
        summary["unhealthy"] = True
        summary["health_failure"] = "reward increased while held-out behavior decreased"
    body = {key: value for key, value in summary.items() if key != "result_hash"}
    summary["result_hash"] = _hash(body)
    _write_atomic_json(metrics_path, summary)
    _write_atomic_json(candidate_dir / "latest_result.json", summary)
    return summary


def _run_candidate_subprocess(
    config_path: Path,
    root: Path,
    candidate: Mapping[str, Any],
    seed: int,
    target_update: int,
    stage: str,
    *,
    method: str | None = None,
    starting_adapter: str | None = None,
) -> dict[str, Any]:
    config = _read_json(config_path)
    job = {
        "schema": f"{SCHEMA}.job.v1",
        "run_dir": str(root.resolve()),
        "model_path": str(Path(config["model"]["path"]).resolve()),
        "candidate": candidate,
        "seed": seed,
        "sampler_seed": seed * 1009 + 7,
        "target_update": target_update,
        "stage": stage,
        "method": method or candidate["arm"],
        "starting_adapter": starting_adapter,
        "rollout": config["rollout"],
        "evaluation": {
            **config["evaluation"],
            "split": "test" if stage.startswith("confirmation") else "valid",
        },
        "base_behavioral_accuracy": _read_json(root / "frozen_base_behavior.json")[
            "test" if stage.startswith("confirmation") else "valid"
        ]["behavioral_accuracy"],
        "selection": config["selection"],
        "health": config["health"],
        "ppo": {**config["ppo"], **({"learning_rate": 0.0} if method == "zero_learning_rate_control" else {})},
        "optimizer": config["optimizer"],
        "checkpoint_every": config["checkpoint_every"],
        "adapter_contract": config["adapter"],
        "shuffle_rewards": method == "shuffled_reward_control",
    }
    job_dir = root / "job_specs"
    job_dir.mkdir(parents=True, exist_ok=True)
    job_path = job_dir / f"{candidate['socket_id']}-seed-{seed}-to-{target_update}.json"
    _write_atomic_json(job_path, job)
    log_dir = root / "logs" / candidate["socket_id"]
    log_dir.mkdir(parents=True, exist_ok=True)
    with (log_dir / f"seed-{seed}-to-{target_update}.log").open("a", encoding="utf-8") as output:
        result = subprocess.run(
            [sys.executable, "-m", "dataevol.experiments.rl_socket_pipeline", "worker", "--job", str(job_path)],
            cwd=_repo_root(),
            stdout=output,
            stderr=subprocess.STDOUT,
            check=False,
        )
    if result.returncode != 0:
        _write_failure(root, f"candidate job failed: {candidate['socket_id']}", f"return code {result.returncode}; see {log_dir}")
        raise RuntimeError(f"candidate job failed: {candidate['socket_id']} seed={seed}")
    return _read_json(root / "jobs" / candidate["socket_id"] / f"seed-{seed}" / "latest_result.json")


def _real_uniform_lora_positive_control(model_path: str | Path, root: Path) -> dict[str, Any]:
    from mlx_lm import batch_generate, load
    from mlx_lm.sample_utils import make_sampler

    root.mkdir(parents=True, exist_ok=True)
    dataset = root / "dataset"
    dataset.mkdir(parents=True, exist_ok=True)
    for split, count, offset in (("train", 96, 0), ("valid", 32, 1000), ("test", 32, 2000)):
        path = dataset / f"{split}.jsonl"
        rows = [
            {
                "id": f"positive-{split}-{index}",
                "prompt": f"Reply with exactly OK and no other text. Control case {offset + index}.",
                "completion": "OK",
            }
            for index in range(count)
        ]
        path.write_text("".join(json.dumps(row, sort_keys=True) + "\n" for row in rows), encoding="utf-8")
    socket = parameter_matched_uniform(2_400_000)
    model, tokenizer = load(str(model_path))
    test_rows = _read_jsonl(dataset / "test.jsonl")
    prompts = [_prompt_tokens(tokenizer, row["prompt"]) for row in test_rows]
    base_outputs = batch_generate(model, tokenizer, prompts, max_tokens=8, sampler=make_sampler(temp=0.0), verbose=False).texts
    base_accuracy = float(np.mean([output.strip() == "OK" for output in base_outputs]))
    trainer_output = io.StringIO()
    with contextlib.redirect_stdout(trainer_output), contextlib.redirect_stderr(trainer_output):
        training = train_socket_expert(
            model_path=model_path,
            socket=socket,
            data_dir=dataset,
            output_dir=root / "uniform_adapter",
            task="positive-control",
            config=TrainConfig(150, batch_size=1, grad_accumulation=1, max_seq_length=128, learning_rate=2e-4),
            seed=9917,
        )
    adapted, adapted_tokenizer = load(str(model_path))
    _apply_socket(adapted, socket)
    adapted.load_weights(list(mx.load(str(root / "uniform_adapter/adapters.safetensors")).items()), strict=False)
    mx.eval(adapted.parameters())
    adapted_outputs = batch_generate(
        adapted, adapted_tokenizer, prompts, max_tokens=8, sampler=make_sampler(temp=0.0), verbose=False
    ).texts
    adapted_accuracy = float(np.mean([output.strip() == "OK" for output in adapted_outputs]))
    improvement = adapted_accuracy - base_accuracy
    return {
        "task": "exact OK response",
        "base_accuracy": base_accuracy,
        "adapted_accuracy": adapted_accuracy,
        "improvement": improvement,
        "required_improvement": 0.50,
        "passed": improvement >= 0.50,
        "optimizer_updates": training["optimizer_updates"],
        "adapter_sha256": training["adapter_sha256"],
        "trainer_output_tail": trainer_output.getvalue()[-2000:],
    }


def _run_confirmation(
    config_path: Path,
    root: Path,
    config: Mapping[str, Any],
    manifest: Mapping[str, Any],
    active: Sequence[str],
) -> dict[str, Any]:
    ranked = _rank_candidates(root, [candidate for candidate in manifest["candidates"] if candidate["socket_id"] in active], config)
    guided = next(row for row in ranked if row["arm"] == "guided_search")
    random_leader = next(row for row in ranked if row["arm"] == "random_search")
    target = int(config["adapter"]["target_parameters"])
    sockets: dict[str, dict[str, Any]] = {
        "rl_searched_socket": guided["socket"],
        "uniform_lora": _tag_topology(parameter_matched_uniform(target), arm="primary"),
        "sft_topology_fresh_weights": _tag_topology(_sft_socket(config, "sft-topology-fresh"), arm="primary"),
        "sft_topology_sft_weights": _tag_topology(_sft_socket(config, "sft-topology-continued"), arm="primary"),
        "mlp_only_socket": _mechanistic_socket("mlp-only", "C", target),
        "attention_only_socket": _mechanistic_socket("attention-only", "A", target),
        "contiguous_multilayer_socket": _contiguous_socket(target),
        "zero_learning_rate_control": _clone_socket(guided["socket"], "zero-learning-rate"),
        "shuffled_reward_control": _clone_socket(guided["socket"], "shuffled-reward"),
    }
    single_sockets = [parameter_matched_single(layer, target, socket_id=f"single-{layer}") for layer in (2, 6, 10, 15, 20)]
    random_distribution = [row["socket"] for row in ranked if row["arm"] == "random_search"]
    for socket in single_sockets:
        candidate = _tag_topology(socket, arm="single_layer_search")
        for seed in config["search"]["seeds"]:
            _run_candidate_subprocess(
                config_path, root, candidate, int(seed), int(config["confirmation"]["updates"]),
                "single-layer-search", method="single_layer_search",
            )
    best_single = max(
        single_sockets,
        key=lambda socket: np.mean([
            _read_json(root / "jobs" / socket["socket_id"] / f"seed-{seed}" / "latest_result.json")["behavioral_accuracy"]
            for seed in config["search"]["seeds"]
        ]),
    )
    confirmation_seeds = list(config["confirmation"]["seeds"])
    for method, socket in sockets.items():
        candidate = _tag_topology(socket, arm=method)
        for seed in confirmation_seeds:
            starting = config.get("sft_adapter_path") if method == "sft_topology_sft_weights" else None
            _run_candidate_subprocess(
                config_path,
                root,
                candidate,
                int(seed),
                int(config["confirmation"]["updates"]),
                "confirmation",
                method=method,
                starting_adapter=starting,
            )
    candidate = _tag_topology(best_single, arm="best_single_layer")
    for seed in confirmation_seeds:
        _run_candidate_subprocess(config_path, root, candidate, int(seed), int(config["confirmation"]["updates"]), "confirmation", method="best_single_layer")
    for socket in random_distribution:
        candidate = _tag_topology(socket, arm="random_socket_distribution")
        for seed in confirmation_seeds:
            _run_candidate_subprocess(
                config_path, root, candidate, int(seed), int(config["confirmation"]["updates"]),
                "confirmation", method="random_socket_distribution",
            )
    if _confirmation_needs_extra_seeds(root, sockets, best_single, random_distribution, confirmation_seeds, config):
        extra_seeds = list(config["confirmation"]["automatic_extra_seeds"])
        for method, socket in sockets.items():
            candidate = _tag_topology(socket, arm=method)
            for seed in extra_seeds:
                starting = config.get("sft_adapter_path") if method == "sft_topology_sft_weights" else None
                _run_candidate_subprocess(
                    config_path, root, candidate, int(seed), int(config["confirmation"]["updates"]),
                    "confirmation-extra", method=method, starting_adapter=starting,
                )
        candidate = _tag_topology(best_single, arm="best_single_layer")
        for seed in extra_seeds:
            _run_candidate_subprocess(
                config_path, root, candidate, int(seed), int(config["confirmation"]["updates"]),
                "confirmation-extra", method="best_single_layer",
            )
        for socket in random_distribution:
            candidate = _tag_topology(socket, arm="random_socket_distribution")
            for seed in extra_seeds:
                _run_candidate_subprocess(
                    config_path, root, candidate, int(seed), int(config["confirmation"]["updates"]),
                    "confirmation-extra", method="random_socket_distribution",
                )
        confirmation_seeds.extend(extra_seeds)
