def _policy_entropy(logits: mx.array, batch: Any) -> float:
    probabilities = nn.softmax(logits[:, :-1, :], axis=-1)
    log_probabilities = nn.log_softmax(logits[:, :-1, :], axis=-1)
    _, mask = completion_logprobs(logits, batch.tokens, batch.prompt_lengths, batch.completion_lengths)
    return float(masked_mean(-mx.sum(probabilities * log_probabilities, axis=-1), mask))


def _is_unhealthy(metrics: Mapping[str, Any], health: Mapping[str, Any]) -> bool:
    return not math.isfinite(metrics["gradient_norm"])


def _health_abort_reason(history: Sequence[Mapping[str, Any]], health: Mapping[str, Any]) -> str | None:
    configured_kl_window = health.get("kl_abort_window")
    if configured_kl_window is not None:
        kl_window = int(configured_kl_window)
        if len(history) >= kl_window and np.mean(
            [row["kl_divergence"] for row in history[-kl_window:]]
        ) > float(health["kl_threshold"]):
            return f"mean KL exceeded {health['kl_threshold']} over {kl_window} updates"
    else:
        kl_window = int(health.get("kl_abort_consecutive", 3))
        if len(history) >= kl_window and all(
            row["kl_divergence"] > float(health["kl_threshold"])
            for row in history[-kl_window:]
        ):
            return f"KL exceeded {health['kl_threshold']} for {kl_window} consecutive updates"
    variance_window = int(health.get("zero_variance_window", 20))
    if len(history) >= variance_window and np.mean(
        [row["zero_variance_group"] for row in history[-variance_window:]]
    ) > float(health.get("zero_variance_abort_threshold", 0.90)):
        return "sustained zero-variance rollout groups"
    diversity_window = int(health.get("diversity_window", 10))
    if len(history) >= diversity_window and np.mean(
        [row["completion_diversity"] for row in history[-diversity_window:]]
    ) < float(health.get("diversity_abort_threshold", 0.20)):
        return "sustained completion-diversity collapse"
    hard_limit_window = int(health.get("hard_limit_window", 5))
    if len(history) >= hard_limit_window and np.mean(
        [row["hard_limit_fraction"] for row in history[-hard_limit_window:]]
    ) > float(health["hard_limit_fraction_threshold"]):
        return "completion length repeatedly reached the hard limit"
    return None


def _mechanistic_socket(identifier: str, family: str, target: int) -> dict[str, Any]:
    layers = (1, 5, 9, 13, 17, 21)
    total_rank = round(target / FAMILY_PARAMS_PER_RANK[family])
    base_rank, remainder = divmod(total_rank, len(layers))
    entries = [
        {"layer": layer, "family": family, "rank": base_rank + int(index < remainder)}
        for index, layer in enumerate(layers)
    ]
    socket = _raw_socket(identifier, entries, f"mechanistic-{identifier}")
    return _tag_topology(socket, arm=identifier)


def _contiguous_socket(target: int) -> dict[str, Any]:
    layers = (8, 9, 10, 11, 12, 13)
    families = ("A", "B", "C", "A", "B", "C")
    rank = round(target / sum(FAMILY_PARAMS_PER_RANK[family] for family in families))
    socket = _raw_socket(
        "contiguous-multilayer",
        [{"layer": layer, "family": family, "rank": rank} for layer, family in zip(layers, families)],
        "mechanistic-contiguous",
    )
    return _tag_topology(socket, arm="contiguous_multilayer_socket")


def _raw_socket(identifier: str, entries: Sequence[Mapping[str, Any]], generation: str) -> dict[str, Any]:
    normalized = sorted(({"layer": int(row["layer"]), "family": str(row["family"]), "rank": int(row["rank"])} for row in entries), key=lambda row: (row["layer"], row["family"]))
    body = {
        "schema": "dataevol.reusable_lora_socket.v1",
        "socket_id": identifier,
        "model_id": "mlx-community/TinyLlama-1.1B-Chat-v1.0-4bit",
        "model_revision": "01a708812690b4685ab18e3b0a27848f92c50746",
        "generation": generation,
        "entries": normalized,
        "trainable_parameters": sum(FAMILY_PARAMS_PER_RANK[row["family"]] * row["rank"] for row in normalized),
    }
    return {**body, "socket_hash": _hash(body)}


